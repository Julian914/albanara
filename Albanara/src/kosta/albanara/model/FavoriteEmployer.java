package kosta.albanara.model;

public class FavoriteEmployer {

	private int favoriteEmployerSeq;
	private int employeeSeq;
	private int employerSeq;
	
	
	public int getFavoriteEmployerSeq() {
		return favoriteEmployerSeq;
	}
	public void setFavoriteEmployerSeq(int favoriteEmployerSeq) {
		this.favoriteEmployerSeq = favoriteEmployerSeq;
	}
	public int getEmployeeSeq() {
		return employeeSeq;
	}
	public void setEmployeeSeq(int employeeSeq) {
		this.employeeSeq = employeeSeq;
	}
	public int getEmployerSeq() {
		return employerSeq;
	}
	public void setEmployerSeq(int employerSeq) {
		this.employerSeq = employerSeq;
	}

	
	
}
